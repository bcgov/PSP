using Pims.Api.Concepts.Models.Base;

namespace Pims.Api.Concepts.Models.Concepts.Note
{
    /// <summary>
    /// NoteModel class, provides a model to represent notes associated to entities.
    /// </summary>
    public class NoteModel : BaseAuditModel
    {
        #region Properties

        /// <summary>
        /// get/set - The id for this note.
        /// </summary>
        public long Id { get; set; }

        /// <summary>
        /// get/set - The note text contents.
        /// </summary>
        public string Note { get; set; }

        /// <summary>
        /// get/set - Whether the note is generated by the system. Default: false.
        /// </summary>
        public bool IsSystemGenerated { get; set; }

        #endregion
    }
}
