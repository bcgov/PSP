-- Enable/insert the "ARBORIST" type
PRINT N'Enable/insert the "ARBORIST" type'
GO
DECLARE @CurrCd NVARCHAR(20)
SET     @CurrCd = N'ARBORIST'

SELECT PROP_MGMT_ACTIVITY_SUBTYPE_CODE
FROM   PIMS_PROP_MGMT_ACTIVITY_SUBTYPE
WHERE  PROP_MGMT_ACTIVITY_TYPE_CODE    = N'PROPERTYMTC'
   AND PROP_MGMT_ACTIVITY_SUBTYPE_CODE = @CurrCd;

IF @@ROWCOUNT = 1
  UPDATE PIMS_PROP_MGMT_ACTIVITY_SUBTYPE
  SET    IS_DISABLED                = 0
       , CONCURRENCY_CONTROL_NUMBER = CONCURRENCY_CONTROL_NUMBER + 1
  WHERE  PROP_MGMT_ACTIVITY_TYPE_CODE    = N'PROPERTYMTC'
     AND PROP_MGMT_ACTIVITY_SUBTYPE_CODE = @CurrCd;
ELSE
  INSERT INTO PIMS_PROP_MGMT_ACTIVITY_SUBTYPE (PROP_MGMT_ACTIVITY_TYPE_CODE, PROP_MGMT_ACTIVITY_SUBTYPE_CODE, DESCRIPTION)
  VALUES (N'PROPERTYMTC', N'ARBORIST', N'Danger tree removal/arborist report');
GO
