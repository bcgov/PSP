/* -----------------------------------------------------------------------------
Create a temporary table to manage the transfer of lease improvements to 
property improvements.
. . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . 
Author        Date         Comment
------------  -----------  -----------------------------------------------------
Doug Filteau  2025-Nov-12  Initial version.
----------------------------------------------------------------------------- */

SET XACT_ABORT ON
GO
SET TRANSACTION ISOLATION LEVEL SERIALIZABLE
GO
BEGIN TRANSACTION
GO
IF @@ERROR <> 0 SET NOEXEC ON
GO

-- Drop the temporary table.
PRINT N'Drop the temporary table.'
GO
DROP TABLE IF EXISTS dbo.TMP_PROPERTY_IMPROVEMENT
GO
IF @@ERROR <> 0 SET NOEXEC ON
GO

-- Create the temporary table.
PRINT N'Create the temporary table.'
GO
CREATE TABLE dbo.TMP_PROPERTY_IMPROVEMENT (
    PROPERTY_ID BIGINT,
    LEASE_ID BIGINT,
    PROPERTY_IMPROVEMENT_TYPE_CODE NVARCHAR(20),
    STRUCTURE_SIZE NVARCHAR(2000),
    IMPROVEMENT_DESCRIPTION NVARCHAR(2000),
    ADDRESS NVARCHAR(2000),
    CONCURRENCY_CONTROL_NUMBER BIGINT,
    APP_CREATE_TIMESTAMP DATETIME,
    APP_CREATE_USERID NVARCHAR(30),
    APP_CREATE_USER_GUID UNIQUEIDENTIFIER,
    APP_CREATE_USER_DIRECTORY NVARCHAR(30),
    APP_LAST_UPDATE_TIMESTAMP DATETIME,
    APP_LAST_UPDATE_USERID NVARCHAR(30),
    APP_LAST_UPDATE_USER_GUID UNIQUEIDENTIFIER,
    APP_LAST_UPDATE_USER_DIRECTORY NVARCHAR(30),
    DB_CREATE_TIMESTAMP DATETIME,
    DB_CREATE_USERID NVARCHAR(30),
    DB_LAST_UPDATE_TIMESTAMP DATETIME,
    DB_LAST_UPDATE_USERID NVARCHAR(30)
)
GO
IF @@ERROR <> 0 SET NOEXEC ON
GO

-- Populate the temporary table.
PRINT N'Populate the temporary table.'
GO
INSERT INTO dbo.TMP_PROPERTY_IMPROVEMENT (PROPERTY_ID, LEASE_ID, PROPERTY_IMPROVEMENT_TYPE_CODE, STRUCTURE_SIZE, IMPROVEMENT_DESCRIPTION, ADDRESS, CONCURRENCY_CONTROL_NUMBER, APP_CREATE_TIMESTAMP, APP_CREATE_USERID, APP_CREATE_USER_GUID, APP_CREATE_USER_DIRECTORY, APP_LAST_UPDATE_TIMESTAMP, APP_LAST_UPDATE_USERID, APP_LAST_UPDATE_USER_GUID, APP_LAST_UPDATE_USER_DIRECTORY, DB_CREATE_TIMESTAMP, DB_CREATE_USERID, DB_LAST_UPDATE_TIMESTAMP, DB_LAST_UPDATE_USERID)
SELECT   prls.PROPERTY_ID
       , prim.LEASE_ID
       , prim.PROPERTY_IMPROVEMENT_TYPE_CODE
       , prim.STRUCTURE_SIZE
       , prim.IMPROVEMENT_DESCRIPTION
       , prim.ADDRESS
       , prim.CONCURRENCY_CONTROL_NUMBER
       , prim.APP_CREATE_TIMESTAMP
       , prim.APP_CREATE_USERID
       , prim.APP_CREATE_USER_GUID
       , prim.APP_CREATE_USER_DIRECTORY
       , prim.APP_LAST_UPDATE_TIMESTAMP
       , prim.APP_LAST_UPDATE_USERID
       , prim.APP_LAST_UPDATE_USER_GUID
       , prim.APP_LAST_UPDATE_USER_DIRECTORY
       , prim.DB_CREATE_TIMESTAMP
       , prim.DB_CREATE_USERID
       , prim.DB_LAST_UPDATE_TIMESTAMP
       , prim.DB_LAST_UPDATE_USERID
FROM     PIMS_PROPERTY_LEASE       prls JOIN
         PIMS_PROPERTY_IMPROVEMENT prim ON prim.LEASE_ID = prls.LEASE_ID
ORDER BY prim.LEASE_ID
       , prls.PROPERTY_ID
GO
IF @@ERROR <> 0 SET NOEXEC ON
GO

COMMIT TRANSACTION
GO
IF @@ERROR <> 0 SET NOEXEC ON
GO
DECLARE @Success AS BIT
SET @Success = 1
SET NOEXEC OFF
IF (@Success = 1) 
  PRINT 'The database update succeeded'
ELSE 
  BEGIN
  IF @@TRANCOUNT > 0 ROLLBACK TRANSACTION
    PRINT 'The database update failed'
  END
GO
