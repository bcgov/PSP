/* -----------------------------------------------------------------------------
Alter the data in the PIMS_BUSINESS_FUNCTION_CODE table.
. . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . . 
Author        Date         Comment
------------  -----------  -----------------------------------------------------
