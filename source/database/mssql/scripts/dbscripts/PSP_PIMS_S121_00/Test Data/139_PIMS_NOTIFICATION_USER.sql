DECLARE @i   INT = 0
DECLARE @max INT = 1000

WHILE @i < @max
  BEGIN
  SET @i = @i + 1
  INSERT INTO dbo.PIMS_NOTIFICATION_USER (NOTIFICATION_ID, USER_ID, CONCURRENCY_CONTROL_NUMBER, APP_CREATE_TIMESTAMP, APP_CREATE_USERID, APP_CREATE_USER_GUID, APP_CREATE_USER_DIRECTORY, APP_LAST_UPDATE_TIMESTAMP, APP_LAST_UPDATE_USERID, APP_LAST_UPDATE_USER_GUID, APP_LAST_UPDATE_USER_DIRECTORY)
    VALUES
      ((SELECT TOP 1 NOTIFICATION_ID FROM PIMS_NOTIFICATION ORDER BY NEWID()),
       (SELECT TOP 1 USER_ID         FROM PIMS_USER         ORDER BY NEWID()),
       1, getutcdate(), N'PSP-11294', NEWID(), N'PSP-11294', getutcdate(), N'PSP-11294', NEWID(), N'PSP-11294')
  END
GO
