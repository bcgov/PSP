    BEGIN
    CREATE SPATIAL INDEX [PLYFAB_SHAPE_IDX] ON [pmbc].[PMBC_PARCEL_POLYGON_FABRIC] (SHAPE)
      USING GEOMETRY_AUTO_GRID 
      WITH (BOUNDING_BOX =( XMIN =200000, YMIN =300000, XMAX =1900000, YMAX =1800000 ))
    END                   
