GO
IF @@ERROR <> 0 SET NOEXEC ON
GO

IF NOT EXISTS(SELECT * 
              FROM   SYS.INDEXES 
              WHERE  NAME = 'PLYFAB_PID_NUMBER_IDX' 
                 AND OBJECT_ID = OBJECT_ID('[pmbc].[PMBC_PARCEL_POLYGON_FABRIC]'))
    BEGIN
    CREATE NONCLUSTERED INDEX [PLYFAB_PID_NUMBER_IDX] ON [pmbc].[PMBC_PARCEL_POLYGON_FABRIC] ([PID_NUMBER] ASC)
    END
