IF NOT EXISTS(SELECT * 
              FROM   SYS.INDEXES 
              WHERE  NAME = 'PLYFAB_PLAN_NUMBER_IDX' 
                 AND OBJECT_ID = OBJECT_ID('[pmbc].[PMBC_PARCEL_POLYGON_FABRIC]'))
    BEGIN
    CREATE NONCLUSTERED INDEX [PLYFAB_PLAN_NUMBER_IDX] ON [pmbc].[PMBC_PARCEL_POLYGON_FABRIC] ([PLAN_NUMBER] ASC)
    END
GO
