SET XACT_ABORT ON
GO
SET TRANSACTION ISOLATION LEVEL SERIALIZABLE
GO
BEGIN TRANSACTION
GO
IF @@ERROR <> 0 SET NOEXEC ON
GO

-- Insert the "CROWNLND" type
DECLARE @CurrCd NVARCHAR(20)
SET     @CurrCd = N'CROWNLND'

SELECT ACQ_CHKLST_SECTION_TYPE_CODE
FROM   PIMS_ACQ_CHKLST_SECTION_TYPE
WHERE  ACQ_CHKLST_SECTION_TYPE_CODE = @CurrCd;

IF @@ROWCOUNT = 0
  BEGIN
  INSERT INTO PIMS_ACQ_CHKLST_SECTION_TYPE (ACQ_CHKLST_SECTION_TYPE_CODE, DESCRIPTION, DISPLAY_ORDER)
    VALUES
      (N'CROWNLND', N'Crown Land', 3);
  END
GO
IF @@ERROR <> 0 SET NOEXEC ON
GO

-- Alter the "SCTN3AGR" type
DECLARE @CurrCd NVARCHAR(20)
SET     @CurrCd = N'SCTN3AGR'

SELECT ACQ_CHKLST_SECTION_TYPE_CODE
FROM   PIMS_ACQ_CHKLST_SECTION_TYPE
WHERE  ACQ_CHKLST_SECTION_TYPE_CODE = @CurrCd;

IF @@ROWCOUNT = 1
  BEGIN
  UPDATE PIMS_ACQ_CHKLST_SECTION_TYPE 
  SET    DISPLAY_ORDER              = 4
       , CONCURRENCY_CONTROL_NUMBER = CONCURRENCY_CONTROL_NUMBER + 1
  WHERE  ACQ_CHKLST_SECTION_TYPE_CODE = @CurrCd;
  END
GO
IF @@ERROR <> 0 SET NOEXEC ON
GO

-- Alter the "SCTN6XPRP" type
DECLARE @CurrCd NVARCHAR(20)
SET     @CurrCd = N'SCTN6XPRP'

SELECT ACQ_CHKLST_SECTION_TYPE_CODE
