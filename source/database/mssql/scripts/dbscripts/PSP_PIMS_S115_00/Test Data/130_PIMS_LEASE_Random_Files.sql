DECLARE @i   INT = 0
DECLARE @max INT = 28

WHILE @i < @max
  BEGIN
  SET @i = @i + 1
  UPDATE ls
  SET    LS.FILE_APPRAISAL_TYPE_CODE   = (SELECT TOP 1 FILE_APPRAISAL_TYPE_CODE FROM PIMS_FILE_APPRAISAL_TYPE ORDER BY NEWID())
       , ls.FILE_LGL_SRVY_TYPE_CODE    = (SELECT TOP 1 FILE_LGL_SRVY_TYPE_CODE  FROM PIMS_FILE_LGL_SRVY_TYPE  ORDER BY NEWID())
       , ls.CONCURRENCY_CONTROL_NUMBER = ls.CONCURRENCY_CONTROL_NUMBER + 1
  FROM PIMS_LEASE ls INNER JOIN 
       (SELECT TOP 50 PERCENT LEASE_ID
        FROM   PIMS_LEASE
        ORDER BY NEWID()) rando ON ls.LEASE_ID = rando.LEASE_ID;
  END
GO
